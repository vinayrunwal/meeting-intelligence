#!/usr/bin/env python3
"""
Meeting Intelligence System — WER Evaluator
===========================================
Computes Word Error Rate (WER) using the jiwer library
to benchmark the ASR engine's accuracy.
"""

import logging
from typing import List

import jiwer

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)


def evaluate_wer(references: List[str], hypotheses: List[str]) -> dict:
    """
    Calculate Word Error Rate (WER) between ground truth references
    and ASR hypotheses.

    Parameters
    ----------
    references : List[str]
        Ground truth transcripts.
    hypotheses : List[str]
        Transcripts generated by the ASR engine.

    Returns
    -------
    dict
        Dictionary containing overall WER and detailed error metrics.
    """
    if len(references) != len(hypotheses):
        raise ValueError("Number of references must match number of hypotheses.")

    if not references:
        return {"wer": 0.0, "insertions": 0, "deletions": 0, "substitutions": 0}

    # Standard preprocessing: lowercase, remove punctuation, remove multiple spaces
    transform = jiwer.Compose([
        jiwer.ToLowerCase(),
        jiwer.RemovePunctuation(),
        jiwer.RemoveMultipleSpaces(),
        jiwer.Strip(),
        jiwer.ReduceToListOfListOfWords(),
    ])

    logger.info(f"Evaluating WER on {len(references)} samples...")
    
    out = jiwer.process_words(
        references,
        hypotheses,
        reference_transform=transform,
        hypothesis_transform=transform,
    )

    metrics = {
        "wer": out.wer,
        "mer": out.mer, # Match Error Rate
        "wil": out.wil, # Word Information Lost
        "insertions": out.insertions,
        "deletions": out.deletions,
        "substitutions": out.substitutions,
        "hits": out.hits,
    }
    
    logger.info(f"Overall WER: {out.wer:.2%}")
    return metrics
